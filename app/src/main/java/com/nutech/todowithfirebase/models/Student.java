package com.nutech.todowithfirebase.models;

public class Student {

    public String name;
    public int age;
    public int rollNo;
    public String title;



    public Student(String name, int age, int rollNo, String title) {
        this.name = name;
        this.age = age;
        this.rollNo = rollNo;
        this.title = title;
    }
}

